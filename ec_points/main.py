from py_ecc.bn128.bn128_curve import G1, G2, multiply, neg


def main():
    a = 4
    b = 3
    c = 6
    d = 2

    # negate G1 * a to make the equation sum up to 0
    aG1 = neg(multiply(G1, a))
    assert aG1 is not None
    # x: (3010198690406615200373504922352659861758983907867017329644089018310584441462,
    # y: 17861058253836152797273815394432013122766662423622084931972383889279925210507)

    bG2 = multiply(G2, b)
    assert bG2 is not None
    # (
    # x1: (2725019753478801796453339367788033689375851816420509565303521482350756874229,
    # x2: 7273165102799931111715871471550377909735733521218303035754523677688038059653),
    # y1: (2512659008974376214222774206987427162027254181373325676825515531566330959255,
    # y2: 957874124722006818841961785324909313781880061366718538693995380805373202866)
    # )

    cG1 = multiply(G1, c)
    assert cG1 is not None
    # x: (4503322228978077916651710446042370109107355802721800704639343137502100212473,
    # y: 6132642251294427119375180147349983541569387941788025780665104001559216576968)

    dG2 = multiply(G2, d)
    assert dG2 is not None
    # (
    # x1: (18029695676650738226693292988307914797657423701064905010927197838374790804409,
    # x2: 14583779054894525174450323658765874724019480979794335525732096752006891875705),
    # y1: (2140229616977736810657479771656733941598412651537078903776637920509952744750,
    # y2: 11474861747383700316476719153975578001603231366361248090558603872215261634898)
    # )

    # Write points to file
    output = f"""aG1_x = {aG1[0]},
aG1_y = {aG1[1]},

bG2_x1 = {bG2[0].coeffs[0]},
bG2_x2 = {bG2[0].coeffs[1]},
bG2_y1 = {bG2[1].coeffs[0]},
bG2_y2 = {bG2[1].coeffs[1]},

cG1_x = {cG1[0]},
cG1_y = {cG1[1]},

dG2_x1 = {dG2[0].coeffs[0]},
dG2_x2 = {dG2[0].coeffs[1]},
dG2_y1 = {dG2[1].coeffs[0]},
dG2_y2 = {dG2[1].coeffs[1]}"""

    with open("points", "w") as f:
        f.write(output)

    print("Points written to 'points' file")


if __name__ == "__main__":
    main()
